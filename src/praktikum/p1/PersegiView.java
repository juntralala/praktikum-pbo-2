    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        try {
            var persegi = new Persegi(Double.parseDouble(sisiField.getText()));
            luasField.setText(String.format("%.2f m^2", persegi.getLuas()));
            kelilingField.setText(String.format("%.2f m", persegi.getKeliling()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Kesalahan Input", "Input harus berupa angka", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hitungActionPerformed

